# Arithmetic Operators:

# Addition: +
# Subtraction: -
# Multiplication: *
# Floor Division: //
# Exponentiation: **
# Modulus: %

# Example:

num1 = 10
num2 = 5

# Addition

sum = num1 + num2
print("Addition: ", sum)

# Subtraction

diff = num1 - num2
print("Subtraction: ", diff)

# Multiplication

product = num1 * num2
print("Multiplication: ", product)

# Floor Division

floor_division = num1 // num2
print("Floor Division: ", floor_division)

# Division

division = num1 / num2
print("Division: ", floor_division)

# Exponentiation

exponentiation = num1 ** num2
print("Exponentiation: ", exponentiation)

# Modulus

modulus = num1 % num2
print("Modulus: ", modulus)

# Order of Operations (PEMDAS/BODMAS):

# B : Brackets: (), {}, []
# O : Of
# D : Division: / , //, %
# M : Multiplication: *, **
# A : Addition: +
# S : Subtraction: -

print(2 + 4 * 3)
print(4 * 3 + 2 - 8 / 2)
print(5 * 4 + 4 - 12 + 1)


